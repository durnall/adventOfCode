var puzzleInputs = puzzleInput.Split("\r\n");

var stacks = new List<string>();
var movements = new List<string>();

var breakFound = false;

for (int i = 0; i < puzzleInputs.Length; i++)
{
    // process every line in the input
    var input = puzzleInputs[i];

    // each section is separated by an empty line
    if (input.Length == 0)
    {
        breakFound = true;
        continue;
    }

    if (breakFound == false)
    {
        // the first section is the stack/crate input
        stacks.Add(input);
    }
    else
    {
        // the second half is the movement input
        movements.Add(input);
    }
}

var listStackPositions = new List<int>();

// final item has the stack number - use the position of the stack numbers to indicate the position of the Crate values per row
var stackPositions = stacks.Last();
var stackCrates = new Dictionary<int, Stack<string>>();
var stackCratesPart2 = new Dictionary<int, Stack<string>>();

for (int i = 0; i < stackPositions.Length; i++)
{
    if (int.TryParse(stackPositions.Substring(i, 1), out int stackNumber))
    {
        for (int j = stacks.Count - 2; j >= 0; j--)
        {
            var crate = stacks[j].Substring(i, 1);

            if (!stackCrates.ContainsKey(stackNumber))
            {
                // add a new stack
                stackCrates.Add(stackNumber, new Stack<string>());
                stackCratesPart2.Add(stackNumber, new Stack<string>());
            }

            if (!string.IsNullOrWhiteSpace(crate))
            {
                // push the crate onto the stack
                stackCrates.Single(stack => stack.Key == stackNumber).Value.Push(crate);
                stackCratesPart2.Single(stack => stack.Key == stackNumber).Value.Push(crate);
            }
        }
    }
}

foreach (var movement in movements)
{
    // move n from x to y
    var movementParts = movement.Split(" ");
    
    var numberOfCrates = int.Parse(movementParts[1]);
    var fromStackNumber = int.Parse(movementParts[3]);
    var toStackNumber = int.Parse(movementParts[5]);
    
    var stackFrom = stackCrates.Single(stack => stack.Key == fromStackNumber).Value;
    var stackTo = stackCrates.Single(stack => stack.Key == toStackNumber).Value;

    var stackFromPart2 = stackCratesPart2.Single(stack => stack.Key == fromStackNumber).Value;
    var stackToPart2 = stackCratesPart2.Single(stack => stack.Key == toStackNumber).Value;
    
    var interimStack = new Stack<string>();
    for (int i = 0; i < numberOfCrates; i++)
    {
        // step 1 - acts like a stack
        stackTo.Push(stackFrom.Pop());

        // step 2 - acts like a queue, emulate one by pushing to an interim stack
        interimStack.Push(stackFromPart2.Pop());
    }
    foreach (var interim in interimStack)
    {
        stackToPart2.Push(interim);
    }
}

